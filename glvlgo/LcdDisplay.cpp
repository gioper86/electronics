class LcdDisplay {

  Adafruit_SSD1306 display;
  
  public:
    LcdDisplay() : display(SCREEN_WIDTH, SCREEN_HEIGHT, &Wire, -1)
    {}

    void initDisplay() {
      if(!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) { // Address 0x3D for 128x64
        Serial.println(F("SSD1306 allocation failed"));
        for(;;);
      }
      delay(500);      
      display.clearDisplay();
      display.setTextSize(1);
      display.setTextColor(WHITE);
      display.setCursor(1, 10);
      display.println("Connecting...");
      display.display();        
    }

    void displayOrderStatus(DeliveryStatus deliveryStatus, int xPosition) {
      display.clearDisplay(); 
      display.setTextSize(1);
      display.setTextColor(WHITE);
      display.setCursor(1, 1);
      //display.println(title);
      display.println("ETA: " + deliveryStatus.eta);
      display.println("");  
      display.println("Status: " + deliveryStatus.orderStatus);
    
      display.drawBitmap(xPosition, 30, epd_bitmap_pizza, 35, 45, WHITE);
      
      display.display(); 
    }    
};
